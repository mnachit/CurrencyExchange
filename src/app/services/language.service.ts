  // Add this method to LanguageService
  getTranslations(language: string): { [key: string]: string } {
    // Return translations for the specific language, or fallback to English
    return this.translations[language] ||
      this.translations[language.split('-')[0]] ||
      this.translations['en'] || {};
  }

  private currentLanguageSubject = new BehaviorSubject<string>(this.getCurrentLanguage());
  public currentLanguage$ = this.currentLanguageSubject.asObservable();

  constructor() {
    // Set initial document direction
    this.setDocumentDirection(this.getCurrentLanguage());
  }

  getCurrentLanguage(): string {
    return localStorage.getItem('userLanguage') || 'fr';
  }

  setLanguage(language: string): void {
    localStorage.setItem('userLanguage', language);
    this.currentLanguageSubject.next(language);
    this.setDocumentDirection(language);
  }

  private setDocumentDirection(language: string): void {
    // Set document direction for RTL languages
    if (language.startsWith('ar')) {
      document.dir = 'rtl';
      document.body.classList.add('rtl');
      document.body.classList.remove('ltr');
    } else {
      document.dir = 'ltr';
      document.body.classList.add('ltr');
      document.body.classList.remove('rtl');
    }
  }

  translate(key: string): string {
    const language = this.getCurrentLanguage();
    // Try to get translation for current language
    const languageData = this.translations[language] || this.translations['en'];
    // Return the translation or the key itself if not found
    return languageData[key] || key;
  }

  // Method to add custom translations (e.g., from API)
  addTranslations(language: string, translations: { [key: string]: string }): void {
    if (!this.translations[language]) {
      this.translations[language] = {};
    }

    // Merge new translations with existing ones
    this.translations[language] = {
      ...this.translations[language],
      ...translations
    };
  }

  // Get available languages for the UI
  getAvailableLanguages(): { code: string, name: string }[] {
    return [
      { code: 'en-US', name: 'English (United States)' },
      { code: 'en-GB', name: 'English (United Kingdom)' },
      { code: 'fr-FR', name: 'French (France)' },
      { code: 'es-ES', name: 'Spanish (Spain)' },
      { code: 'ar-SA', name: 'Arabic (Saudi Arabia)' },
      { code: 'ar-MA', name: 'Arabic (Morocco)' }
    ];
  }

  // Helper methods for formatting based on locale
  formatDate(date: Date): string {
    return date.toLocaleDateString(this.getCurrentLanguage());
  }

  formatCurrency(amount: number, currencyCode: string = 'USD'): string {
    return new Intl.NumberFormat(this.getCurrentLanguage(), {
      style: 'currency',
      currency: currencyCode
    }).format(amount);
  }

  formatNumber(number: number, minimumFractionDigits: number = 2): string {
    return new Intl.NumberFormat(this.getCurrentLanguage(), {
      minimumFractionDigits,
      maximumFractionDigits: minimumFractionDigits
    }).format(number);
  }

  isRtl(): boolean {
    return this.getCurrentLanguage().startsWith('ar');
  }
